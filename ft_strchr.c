#include "fillit.h"

char	*ft_strchr(const char *s, int c)
{
	char	*s1;
	int		i;

	i = -1;
	s1 = (char *)s;
	while (s1[++i] != '\0')
		if (s1[i] == c)
			return (s1 + i);
	if (s1[i] == c)
		return (s1 + i);
	return (NULL);
}
